// Determine the quotient from two numbers input from the keyboard.
#include <iostream>
using namespace std;

int main()
{
    int value1, value2;
    cout << "Enter two numbers: ";
    cin >> value1 >> value2;
    cout << "The quotient of " << value1 << " and " << value2 << " is " << value1 / value2 << endl;
    return 0;
}
